md"""
## Optimal Transport using JuMP

```math
	\inf_{P\in U(\mu,\nu)}\langle P,C\rangle
```
"""

# ╔═╡ 520db44c-a6e5-479d-a44b-6530bfdff2d7
Nμ = 10; Nν = 20

# ╔═╡ 5cd73667-b2f7-4425-96f7-088e3d05b223
dμ = ones(Nμ) / Nμ; dν = ones(Nν) / Nν

# ╔═╡ 52de7e53-7e88-4cd0-91fe-375c921f881f
μ = rand(2, Nμ); ν = rand(2, Nν)

# ╔═╡ 051b7617-7dd6-467e-841b-3ee82c7a47a9
C = pairwise(euclidean, μ, ν)

# ╔═╡ 463d49ce-7abf-4a9c-9504-d88f4c7d4e79
begin
	model = Model(GLPK.Optimizer)

	# The optimal transport plan
	@variable(
		model,
		P[1:Nμ, 1:Nν] >= 0
	)

	# Minimize the tranport cost
	@objective(model, Min, sum(
		P .* C
	))

	# Add the marginal constraints
	@constraints(
		model,
		begin
			[i in 1:Nμ], sum(P[i,:]) == dμ[i]
			[j in 1:Nν], sum(P[:,j]) == dν[j]
		end
	)

	optimize!(model)

	if termination_status(model) != OPTIMAL
		error("Failed to find a solution (status = $(termination_status(model)))")
	end

	emd2 = objective_value(model)
	γ = value.(P)
end;

# ╔═╡ fce525d9-9bb6-4c7b-be9b-085df346a99e
termination_status(model)

